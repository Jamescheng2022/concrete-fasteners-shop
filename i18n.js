// ─── Language Switcher Init ──────────────────────────────────────────────────
let currentLang = localStorage.getItem('proconnect_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('proconnect_lang', lang);
  const t = I18N[lang];
  document.documentElement.lang = lang;
  document.documentElement.dir = t.dir;

  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const val = key.split('.').reduce((obj, k) => obj && obj[k], t);
    if (val !== undefined) {
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.placeholder = val;
      } else {
        el.innerHTML = val;
      }
    }
  });

  // Update lang switcher active state
  document.querySelectorAll('.lang-btn').forEach(btn => {
    btn.classList.toggle('active', btn.dataset.lang === lang);
  });

  // Re-render chat welcome if chat is open
  if (document.getElementById('chat-messages')) {
    const chatWelcome = document.getElementById('chat-welcome');
    if (chatWelcome) chatWelcome.innerHTML = formatChatMsg(t.chat.welcome);
  }
}

function initLang() {
  setLang(currentLang);
}

// ─── Chat Helpers ────────────────────────────────────────────────────────────
function formatChatMsg(text) {
  return text
    .replace(/\*\*(.*?)\*\*/g, '<strong>$1</strong>')
    .replace(/\n/g, '<br>');
}

function appendChatMsg(role, text) {
  const wrap = document.getElementById('chat-messages');
  if (!wrap) return;
  const div = document.createElement('div');
  div.className = `chat-msg chat-msg-${role}`;
  div.innerHTML = `<div class="chat-bubble">${formatChatMsg(text)}</div>`;
  wrap.appendChild(div);
  wrap.scrollTop = wrap.scrollHeight;
  return div;
}

function sendChatMessage() {
  const input = document.getElementById('chat-input');
  if (!input) return;
  const msg = input.value.trim();
  if (!msg) return;
  input.value = '';

  appendChatMsg('user', msg);

  const t = I18N[currentLang].chat;
  const thinkingDiv = appendChatMsg('bot', `<span class="chat-typing"><span></span><span></span><span></span></span>`);

  setTimeout(() => {
    thinkingDiv.querySelector('.chat-bubble').innerHTML = formatChatMsg(aiChatResponse(msg, currentLang));
    document.getElementById('chat-messages').scrollTop = 999999;
  }, 600 + Math.random() * 600);
}
